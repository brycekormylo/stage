struct ImageSlider: View {
    
    let content: [URL]
    let imageHeight: CGFloat
    
    init(_ content: [URL] = sampleImages, imageHeight: CGFloat) {
        self.content = content
        self.imageHeight = imageHeight
    }
    
    @ViewBuilder
    var body: some View {
        ScrollView {
            LazyVStack {
                Spacer(minLength: 180)
                ForEach(content, id: \.self) { imageURL in
                    SlidingImage(imageURL, startingHeight: imageHeight)
                }
                Spacer(minLength: 240)
            }
        }
    }
}

struct ImageSliderPreviewFormatted: View {
    var body: some View {
        ZStack {
            Color.black.ignoresSafeArea()
            ImageSlider(imageHeight: 200)
                .padding(.horizontal)
        }
    }
}

